public class MonthlyMenu {
	
	private int week;
	private String cat;
	private String food;
	private String drink;
	
	public MonthlyMenu(int week, String cat, String food, String drink) {
		this.week = week;
		this.cat = cat;
		this.food = food;
		this.drink = drink;
	}

	public int getWeek() {
		return week;
	}

	public String getCat() {
		return cat;
	}

	public String getFood() {
		return food;
	}

	public String getDrink() {
		return drink;
	}
	
	
	
	
	
}